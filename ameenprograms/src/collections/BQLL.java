package collections;
import java.util.*;
public class BQLL {

	public static void main(String[] args) {
		LinkedList l1=new LinkedList();
		l1.offer(10);
		l1.offer(20);
		l1.offer(30);
		System.out.println(l1);
		System.out.println(l1.peek());
		System.out.println(l1);
		System.out.println(l1.poll());
		System.out.println(l1);
		System.out.println(l1.peekFirst());

	}

}
